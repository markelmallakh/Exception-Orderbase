/* ---------------------------------------------------------------------------
   Circular Gallery — vanilla ES-module port of React Bits <CircularGallery/>
   (WebGL via ogl). Labels REMOVED per the design (images only). Boots itself
   on any [data-circular-gallery] element and loops the Exception image set.

   ogl is loaded from a CDN because the site has no build step / npm — same
   approach as the Tailwind Play CDN and Google Fonts already in use.
   --------------------------------------------------------------------------- */
import {
  Camera,
  Mesh,
  Plane,
  Program,
  Renderer,
  Texture,
  Transform,
} from "https://cdn.jsdelivr.net/npm/ogl@1.0.11/+esm";

